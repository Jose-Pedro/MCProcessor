-- Identity stub tables for local HANA boot.
-- Mirrors the SQLite stubs in scripts/start-srv.cjs.
-- These tables live in cnx_external in production and are absent from cmm-sync dumps.
-- Empty stubs are enough for the per-request user bootstrap and the MANAGERS / PMO_MANAGERS / REQUESTERS views.

DROP TABLE "INTPROJ"."US_USERS_IAS";
CREATE COLUMN TABLE "INTPROJ"."US_USERS_IAS" (
    USER_ID NVARCHAR(255) PRIMARY KEY,
    USER_NAME NVARCHAR(255),
    EMAIL NVARCHAR(255),
    TELEPHONE NVARCHAR(255),
    NEDAP NVARCHAR(255),
    ILOQ NVARCHAR(255),
    CARD_NUMBER NVARCHAR(255)
);

DROP TABLE "INTPROJ"."US_ROLES_AGR";
CREATE COLUMN TABLE "INTPROJ"."US_ROLES_AGR" (
    USER_ID NVARCHAR(255),
    IAS_GROUP NVARCHAR(255),
    PRIMARY KEY (USER_ID, IAS_GROUP)
);

DROP TABLE "INTPROJ"."US_BUKS";
CREATE COLUMN TABLE "INTPROJ"."US_BUKS" (
    USER_ID NVARCHAR(255),
    BUK NVARCHAR(255),
    PRIMARY KEY (USER_ID, BUK)
);

DROP TABLE "INTPROJ"."US_COUNTRIES";
CREATE COLUMN TABLE "INTPROJ"."US_COUNTRIES" (
    USER_ID NVARCHAR(255),
    COUNTRY_ID NVARCHAR(255),
    PRIMARY KEY (USER_ID, COUNTRY_ID)
);

DROP TABLE "INTPROJ"."US_ZAGENCY";
CREATE COLUMN TABLE "INTPROJ"."US_ZAGENCY" (
    USER_ID NVARCHAR(255),
    ZAGENCY_ID NVARCHAR(255),
    ZAGENCY_DESCRIPTION NVARCHAR(255),
    PRIMARY KEY (USER_ID, ZAGENCY_ID)
);

DROP TABLE "INTPROJ"."US_ZCUSTOMER";
CREATE COLUMN TABLE "INTPROJ"."US_ZCUSTOMER" (
    USER_ID NVARCHAR(255),
    ZCUSTOMER_ID NVARCHAR(255),
    ZCUSTOMER_DESCRIPTION NVARCHAR(255),
    PRIMARY KEY (USER_ID, ZCUSTOMER_ID)
);

DROP TABLE "INTPROJ"."US_ZVENDOR";
CREATE COLUMN TABLE "INTPROJ"."US_ZVENDOR" (
    USER_ID NVARCHAR(255),
    ZVENDOR_ID NVARCHAR(255),
    ZVENDOR_DESCRIPTION NVARCHAR(255),
    PRIMARY KEY (USER_ID, ZVENDOR_ID)
);
